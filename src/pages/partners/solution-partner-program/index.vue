<script>
    import AppMeta from '~/components/AppMeta.vue';
    import AppHeader from '~/components/AppHeader.vue';
    import AppFooter from '~/components/AppFooter.vue';
    import hsForm from '~/components/hsForm.vue';

    export default {
        components: {
            'app-meta': AppMeta,
            'app-header': AppHeader,
            'app-footer': AppFooter,
            'hs-form': hsForm
        },

        mounted() {

            /*Affix*/
            // For Affix
            // Scrollspy
             // For Affix
            // Scrollspy
            $('body').scrollspy({ target: '#ca-nav-affix'});  

            // Affix onclick to show the section
            $("#ca-nav-affix .cg-affix__item a[href^='#']").on('click', function(e) {
                console.log(e)
               // prevent default anchor click behavior
               e.preventDefault();

               // store hash
               var hash = this.hash;

               // animate
                $('html, body').animate({
                   scrollTop: $(this.hash).offset().top
                 }, 1000, function(){

                   // when done, add hash to url
                   // (default click behaviour)
                   window.location.hash = hash;
                });
            });

            //Affix scroll
            $('[data-affix-after]').each( function() {
                var elem = $(this);
                var parent_panel = elem.parent();
                var prev = $( elem.data('affix-after') );

                if( prev.length != 1 ) {
                /* Create a new element immediately before. */
                prev = elem.before( '<div></div>' ).prev();
                }

                var resizeFn = function() {
                  /* Set the width to it's natural width in the parent. */
                  var sideBarNavWidth = parent_panel.width()
                      - parseInt(elem.css('paddingLeft'))
                      - parseInt(elem.css('paddingRight'))
                      - parseInt(elem.css('marginLeft'))
                      - parseInt(elem.css('marginRight'))
                      - parseInt(elem.css('borderLeftWidth'))
                      - parseInt(elem.css('borderRightWidth'));
                  elem.css('width', sideBarNavWidth);

                  elem.affix({
                    offset: {
                        top: prev.offset().top + prev.outerHeight(true) + 110,
                        bottom: function () {
                            return (this.bottom = $('#cb-affix-footer').outerHeight(true) + $('.cg-footer').outerHeight(true) + 120)
                        }
                    }
                  });
                };

                resizeFn();
                    $(window).resize(resizeFn);
            });

            $('.cg-hero__actiona').on('click', function(){
                $('html, body').animate({
                    scrollTop: $(".cg-article").offset().top - 150
                }, 500);
            })

            /*Affix*/

            $(window).on('load', function(){
                setTimeout(function(){
                    $('.cp-hero__highlight').addClass('cp-hero__highlight--active')
                }, 750)

                //For rellax js parallax
                // var rellax = new Rellax('.rellax');
            })

            // For modal
            $('.cc-popup__show').click(function(){
                $(window).scrollTop(0);
                $('body').addClass('no-scroll');
                var targetSelector = $(this).attr('target-element');
                $(targetSelector).addClass('cc-popup__visible');
            })
            $('.cc-popup__close').click(function(){
                $('body').removeClass('no-scroll');
                $('.cc-popup-section').removeClass('cc-popup__visible');
            })
            $(document).keyup(function(e) {
                if (e.keyCode === 27){
                    $('body').removeClass('no-scroll');
                    $('.cc-popup-section').removeClass('cc-popup__visible');
                }
            });

            //For Panel = FAQ list click
            $('[data-panel]').click(function() {
                $(this).toggleClass('cp-tpanel--active').siblings().removeClass('cp-tpanel--active');
            });


            //Trigger Drift
            $("#email_validation").on('click', function(){
                drift.api.startInteraction({ interactionId: 111909 });
            });


            /*Smooth Scrolling*/
                $('.pa__link').click(function($e) {
                $e.preventDefault();
                $('html, body').animate({
                    scrollTop: $('.scroll-div').offset().top  
                }, 1000);
                });  
            /*Smooth Scrolling*/
        }
    }
</script>
