        mounted() {
            $(window).on('load', function(){
                $('.cp-slider').each(function(){
                    var thisSlider = $(this);
                    thisSlider.find('.cp-slider__item:first-child').addClass('cp-slider__item-active');
                    var imgSrc = thisSlider.find('.cp-slider__item-active').attr('get_url');
                    thisSlider.find('[set_url]').attr('src',imgSrc);
                })
                // For Slider in page
                var activeItem;
                function slider(){
                    $('.cp-slider').each(function(){
                        var selectThisSlider = $(this);
                        activeItem = selectThisSlider.find('.cp-slider__item-active').next('.cp-slider__item');
                        if(activeItem.length){
                            selectThisSlider.find('.cp-slider__item').removeClass('cp-slider__item-active');
                            $(activeItem).addClass('cp-slider__item-active');
                        }else{
                            selectThisSlider.find('.cp-slider__item').removeClass('cp-slider__item-active');
                            selectThisSlider.find('.cp-slider__item:first-child').addClass('cp-slider__item-active');
                        }
                        var selectImgSrc = selectThisSlider.find('.cp-slider__item-active').attr('get_url');
                        selectThisSlider.find('[set_url]').attr('src',selectImgSrc);
                    })
                }
                var startSlider = setInterval(slider, 6000);
                $('.cp-slider__item .cp-slider__heading').on('click', function(){
                    var getThisSlider = $(this).parents('.cp-slider');
                    getThisSlider.find('.cp-slider__item-active').removeClass('cp-slider__item-active');
                    clearInterval(startSlider);
                    $(this).parents('.cp-slider__item').addClass('cp-slider__item-active');
                    $('.cp-slider__item-active').find('.cp-slider__progress').addClass('cp-slider__progress--hide');
                    // startSlider = setInterval(slider, 4000);
                    var getImgSrc = getThisSlider.find('.cp-slider__item-active').attr('get_url');
                    getThisSlider.find('[set_url]').attr('src',getImgSrc);
                })
            });
            function renderSkrollr(){
                var winWidth = $( window ).width();
                if(winWidth > 991){
                    if( /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent) ) {
                        // console.log('Yes this is touch device')
                        $('#heroImg').show();
                        $('#heroSvg').remove();
                    }else{
                        // console.log('No, this is not touch device')
                        var sklr = skrollr.init({
                            smoothScrolling: false
                        });
                    }
                }
            }
            renderSkrollr()
            $(window).on('resize', function(){
                renderSkrollr()
            });
        }
    }
