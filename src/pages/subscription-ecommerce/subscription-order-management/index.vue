import AppMeta from '~/components/AppMeta.vue';
import AppHeader from '~/components/AppHeader.vue';
import AppFooter from '~/components/AppFooter.vue';

export default {
    components: {
    'app-meta': AppMeta,
    'app-header': AppHeader,
    'app-footer': AppFooter
    },
    // jQuery methods/functions should be placed inside the methods
    methods: {},
    // jQuery interaction should be placed inside the mounted method
    mounted() { 
        const mainGraphic = document.getElementById('js-main-graphic');
        let master = new TimelineMax();

        function webstoreIntro() {
        const
            boxA                  = mainGraphic.querySelector('.box--a'),
            boxB                  = mainGraphic.querySelector('.box--b'),
            boxC                  = mainGraphic.querySelector('.box--c'),
            trally                = mainGraphic.querySelector('.trally'),
            trallyWheels          = mainGraphic.querySelectorAll('.trally__wheel'),
            trallyWaves           = mainGraphic.querySelectorAll('.trally--wave'),
            webstoreFrame         = mainGraphic.querySelector('.webstore__frame'),
            webstoreDots          = mainGraphic.querySelectorAll('.webstore__dot'),
            chargebee             = mainGraphic.querySelector('.chargebee'),
            platfromFrame         = mainGraphic.querySelector('.platform__frame'),
            platformDots          = mainGraphic.querySelectorAll('.platform__dot'),
            vanFrame              = mainGraphic.querySelector('.van__frame'),
            vanWheels             = mainGraphic.querySelectorAll('.van__wheel'),
            vanWaves              = mainGraphic.querySelectorAll('.van__wave'),

            // Lines & Arrows
            downStoreToCb         = mainGraphic.querySelector('.webstore-chargebee'),
            downCbToPlatform      = mainGraphic.querySelector('.chargebee-platform'),
            upPlatformToCb        = mainGraphic.querySelector('.platform-chargebee'),
            upCbToStore           = mainGraphic.querySelector('.chargebee-webstore'),
            arrowsA               = mainGraphic.querySelectorAll('.arrow--a'),
            arrowsB               = mainGraphic.querySelectorAll('.arrow--b'),
            arrowsC               = mainGraphic.querySelectorAll('.arrow--c'),
            arrowsD               = mainGraphic.querySelectorAll('.arrow--d'),

            // Names
            webstoreName          = mainGraphic.querySelector('.webstore__name'),
            platFormName          = mainGraphic.querySelectorAll('.platform__name');


        var webstoreAnim = new TimelineMax()
            .set([arrowsA, arrowsB], {transformOrigin: '50% 50%', y: -16, alpha: 0})
            .set([arrowsC, arrowsD], {transformOrigin: '50% 50%', y: 16, alpha: 0})
            .set(vanWheels, {transformOrigin: '50% 50%',alpha: 0, x: -60, rotation: -720})
            .set(trallyWheels, {x: -30, alpha: 0})
            .set([downStoreToCb, downCbToPlatform, upPlatformToCb, upCbToStore, webstoreName, platFormName], {alpha:0})
            // Webstore -- Reveal
            .fromTo(webstoreFrame, .4, {x: 10, scale: .8, alpha: 0}, {x:0, scale: 1, alpha: 1}, 'mainanim')
            .staggerFromTo(webstoreDots, .4, {alpha:0}, {alpha: 1}, .2, 'mainanim+=.2')
            .fromTo(trally, .6, {x: -40, alpha: 0}, {x:0, alpha: 1, onStart: function(){
            TweenMax.staggerFromTo(trallyWheels, .3, {x: -30, alpha: 0}, {x: 0, alpha: 1}, .06)
            }}, 'mainanim+=.3')
            .staggerFromTo(trallyWaves, .4, {x: -10, alpha: 0},{x: 0, alpha: 1}, .2, 'mainanim+=.38')
            .fromTo(boxB, .3, {y: -24, alpha: 0},{y: 0, alpha: 1}, 'mainanim+=.45')
            .fromTo(boxA, .3, {y: -20, alpha: 0}, {y: 0, alpha: 1}, 'mainanim+=.55')
            .fromTo(boxC, .3, {y: -16, alpha: 0}, {y: 0, alpha: 1}, 'mainanim+=.65')
            // Webstore Name -- Reveal
            .fromTo(webstoreName, .4, {alpha: 0}, {alpha: 1}, 'mainanim+=.72')
            // Chargebee
            .fromTo(chargebee, .6,
            {transformOrigin: '35.439 35.439',rotation: -360, scale: .7, alpha: 0},
                {transformOrigin: '35.439 35.439',rotation: 0, scale: 1, alpha: 1}, 'mainanim+=.9')
            // Line & Arrow
            .fromTo(downStoreToCb, .4, {alpha: 0}, {alpha: 1}, 'mainanim+=1')
            .to(this,0,{onComplete:function(){TweenMax.set(arrowsA,{className: 'st6 arrow arrow--a arrowDown'})}}, 'mainanim+=1.2')
            // Van -- Reveal
            .fromTo(platfromFrame, .4, {x: 10, scale: .8, alpha: 0}, {x:0, scale: 1, alpha: 1}, 'mainanim+=1.6')
            .staggerFromTo(platformDots, .4, {alpha:0}, {alpha: 1}, .2, 'mainanim+=1.8')
            .fromTo(vanFrame, .6, {alpha: 0, x: -60}, {alpha: 1, x: 0, onStart: function(){
            TweenMax.staggerTo(vanWheels, .6,
                {transformOrigin: '50% 50%',alpha: 1, x: 0, rotation: 0})
            }}, 'mainanim+=1.4')
            .staggerFromTo(vanWaves, .4, {x: -10, alpha: 0},{x: 0, alpha: 1}, .2, 'mainanim+=1.6')
            // Webstore Name -- Reveal
            .staggerFromTo(platFormName, .4, {alpha: 0}, {alpha: 1}, 'mainanim+=.1.8')
            // Line & Arrow
            .fromTo(downCbToPlatform, .4, {alpha: 0}, {alpha: 1}, 'mainanim+=2.4')
            .to(this,0,{onComplete:function(){TweenMax.set(arrowsB,{className: 'st6 arrow arrow--b arrowDown'})}}, 'mainanim+=2.6')
            // Line & Arrow
            .fromTo(upPlatformToCb, .4, {alpha: 0}, {alpha: 1}, 'mainanim+=3')
            .to(this,0,{onComplete:function(){TweenMax.set(arrowsC,{className: 'st3 arrow arrow--c arrowUp'})}}, 'mainanim+=3.2')
            // Line & Arrow
            .fromTo(upCbToStore, .4, {alpha: 0}, {alpha: 1}, 'mainanim+=3.6')
            .to(this,0,{onComplete:function(){TweenMax.set(arrowsD,{className: 'st3 arrow arrow--d arrowUp'})}}, 'mainanim+=3.8')

            return webstoreAnim
        }
        master.add(webstoreIntro)
    }
}
