                            <div class="cg-article">
                                <div class="cg-article__header">
                                    <h3 class="cg-article__heading">
                                        Tiered Pricing Model:
                                    </h3>
                                </div>

                                <div class="cg-article__content">
                                    <p class="cg-article__desc">
                                        Tiered pricing model is based on the tiered pricing strategy when it comes to the real world pricing scenario. Both terms are used interchangeably, leading to a potential confusion. <a href="/resources/glossaries/tiered-pricing-model-strategy/" class="cg-link">This post spells the difference between the two.</a> In the tiered pricing model different versions of the product is offered at varied prices. These can be based on features, number of users or usage and is decided by the business based on the product they have to offer. Usually 2 to 5 tiers are created and your customers are allowed to choose from the same based on their needs. Using this pricing model you can upsell to your customers incrementally which can include features as they scale. The pricing tiers should be carefully constructed with an appropriate strategy failing which your customer will end up getting confused. 
                                    </p>

                                    <p class="cg-article__desc">
                                        <strong>Example:</strong> <a href="https://www.hubspot.com/pricing/marketing/" class="cg-link">Hubspot</a> is a developer and marketer of software products which are used for inbound marketing and sales activities. They’ve priced their plans useful in such a way that new marketers to professional teams can use their product as they scale. 
                                    </p>
                                    
                                    <figure class="cg-article__figure" style="margin-bottom: 60px;margin-top: 50px;">
                                        <img src="/static/website/version/images/resources/guides/saas-pricing-models/saas-pricing-models-2.png" alt="" class="img-responsive" />
                                    </figure>

                                    <p class="cg-article__desc">
                                        <strong>Advantages of tiered pricing model: </strong>
                                    </p>
                                    <ul class="cg-article__list">
                                        <li class="cg-article__item">
                                            It appeals to different customers from beginners to advanced, thereby increase your market share and revenue potential. 
                                        </li>
                                        <li class="cg-article__item">
                                            Gives your customers the power of choice, so that they can pick and choose the plan that works for them.. 
                                        </li>
                                        <li class="cg-article__item">
                                            Opportunity to upsell to your existing customers that’ll improve your existing MRR without any marketing spend. 
                                        </li>
                                    </ul>
                                    <p class="cg-article__desc">
                                        <strong>Disadvantages of tiered pricing model:</strong>
                                    </p>
                                    <ul class="cg-article__list">
                                        <li class="cg-article__item">
                                            The tiers should be carefully thought out and constructed based on the customers you’re targeting. A lot of tiers can be confusing whereas lesser number of poorly constructed tiers doesn’t really help you reap the benefits of the tiered model.
                                        </li>
                                        <li class="cg-article__item">
                                            Every tier increases the complexity of your customers’ decision making process. 
                                        </li>
                                    </ul>
