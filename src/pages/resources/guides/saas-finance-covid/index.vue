                                    <div class="cg-article__desc">
                                       <p> MW: Forecasting is done based on assumptions and those assumptions are the key. </p>
                                       <ul class="cg-article__desc cg-article__dash"> 
                                            <li>
                                                The new thing about this landscape is that the baseline has moved and we don’t know how much our baseline has moved. It's always best to be more aggressive up front and assuming how bad things might be in terms of the new world we're in.
                                            </li>
                                            <li>
                                                The other side of this situation is that we’re not sure how long we’re going to be at that bottom of the trough. If it seems long enough, ask “How long will this last for?”. That would be the other major assumption going into this model. And then look at which road back to the new normal are you also assuming is going to occur?  Is it going to be fast? Is it going to be slow? Will it be sooner? Will it be later?
                                            </li>
                                            <li>
                                                The general takeaway from this would be, anytime you’re talking about a forecast you need to have clarity on the assumptions you're making about each of those. 
                                            </li>
                                            <li>
                                                To be clear about a forecast you need to know the answer to these three questions: how bad is the new baseline, how long will we be there? And what are we assuming about recovery?
