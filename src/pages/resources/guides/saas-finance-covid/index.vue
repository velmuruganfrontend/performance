                                        <p class="cg-article__desc">
                                            We cannot dispute the fact that even well-run companies are going bankrupt. Suppliers in the hospitality industry, they had to shut down their entire business overnight. At best they planned to cut out 70-80% of their staff and if they trudged along the bad waters for too long, they simply had to close their business. You can imagine the hot seat their finance director was sitting on.  
                                        </p>
