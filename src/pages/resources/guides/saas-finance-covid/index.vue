                                        </p>
                                         <p class="cg-article__desc">
                                             Companies aren’t spending unless they’re legally obliged to or if it is business-critical. Every other cost that’s not immediate is put in hibernation as companies try to survive the crisis and hopefully emerge in better shape than their competition.
