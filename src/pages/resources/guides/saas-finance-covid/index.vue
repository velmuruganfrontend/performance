                                        Cash flow analysis
                                    </h3>
                                </div>
                                 <div class="cg-article__content">
                                    <p class="cg-article__desc">
                                        Cash flow analysis, in simple terms, is the amount of money flowing in and out of your business. It measures the liquidity of your business.  Analyzing your cash-flow is important during normal times, but during these volatile times, it should garner extra attention and focus. 
                                    </p>
                                    <p class="cg-article__desc">
                                        BM: Cash flow is measured as net cash flow and gross cash flow. It helps to analyze if you’re burning cash or building cash. Cash flow is the major reason why many businesses fail during good and bad times. Hence, it’s crucial to understand cash runaway. I like to look at cash burn like no invoicing was being done, in simpler terms, no cash is coming in. 
                                    </p>
                                     <p class="cg-article__desc">
                                         To calculate, look at your current bank balance and divide it by monthly operating expenses. This will give you an idea of how many months of cash runway do you have? If your business didn’t invoice anymore, how long could you thrive with the runway amount?  Delving a bit deeper into cash-flow, these are the main components you should be looking at, 
                                    </p>
                                    <figure class="cg-article__figure" style="margin-bottom:60px;margin-top:50px">
