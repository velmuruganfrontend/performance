                                         These areas are our core business activities and ties down to the invoices we’re sending out to customers. The first is “operating activities”, which covers the operating expenses such as wages, rent, etc., Then comes “investing activities”. If you’re investing in servers, internal R&D, etc., From the “financial activities” perspective, it could be cash coming in from loans, stocks, or just principal payments on our term loans. 
