                                        </p>
                                        <p class="cg-article__desc">
                                            Many companies are feeling the crisis in different paces from day one spilling over into every passing day. It’s only natural to feel like the Lehman Brothers in this crisis! This means your plan A, B, and C made at the beginning of the year feels useless and now you’re in a prolonged survival mode. Here are a few tips on how you can restructure and budget effectively. 
                                        </p>
