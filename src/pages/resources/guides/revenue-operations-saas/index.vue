                                       The Shift from Streams to Cycles
                                    </h2>
                                </div>
                                <div class="cg-article__content">
                                    <p class="cg-article__desc">  
                                       Before the birth of SaaS, Revenue flow was unidirectional - from Marketing, Sales, to Finance. While Marketing and Sales were seen as the revenue generating teams, Finance was just a collections function.
                                    </p>
                                    <figure class="cg-article__figure" style="margin-bottom: 30px;margin-top: 50px;">  
                                        <img src="/static/website/version/images/resources/guides/revenue-operations-saas/revops7.png" alt="unidirectional revenue flow" class="img-responsive" />
                                    </figure>
                                    <p class="cg-article__desc">
                                        <a href="https://hbr.org/2006/07/ending-the-war-between-sales-and-marketing" target="_blank" class="cg-link">Ending the War Between Sales and Marketing</a>, an article in HBR talks about how only Sales and Marketing are responsible for the funnel that leads customers toward purchases and ongoing relationships. The article talks about how a Chief Revenue Officer is brought in to bring alignment between Sales and Marketing.
                                    </p>
                                    <p class="cg-article__desc">
                                        “The main rationale for integrating Sales and Marketing is that the two functions have a common goal: the generation of profitable and increasing revenue.”
                                    </p>
                                    <p class="cg-article__desc">
                                        While this could be true for transactional businesses, recurring revenue model is not a funnel or a stream. <a class="cg-link" href="https://www.slideshare.net/DavidSkok/the-saas-business-model-and-metrics">Without Front loaded costs</a>, revenue flow in a SaaS business shifts to a cycle.
                                    </p>
                                     <p class="cg-article__desc">
                                       A SaaS business needs to focus on acquiring new customers and also retaining them. Now the responsibility gets split not just between Marketing and Sales but also Customer Success, Support and Finance.
                                     </p>
                                    <figure class="cg-article__figure" style="margin-bottom: 25px;margin-top: 50px;">  
                                        <img src="/static/website/version/images/resources/guides/revenue-operations-saas/revops1.png" alt="saas revenue cycle" class="img-responsive" />
                                    </figure>
                                    <p class="cg-article__desc">
                                        The shift in how revenue flows in a SaaS business changes a lot of things fundamentally.
                                    </p>
                                    <ol class="cg-article__list">
                                        <li class="cg-article__item">
                                            The <b>front-loaded costs</b> make it necessary, even critical, for such businesses to focus on making sure the revenue engine keeps running long after a customer is obtained.
                                        </li>
                                        <li class="cg-article__item">
                                            <b>Revenue Cycle</b> requires customers to be continuously engaged throughout their lifecycle to drive retention, encourage expansion and minimize churn.
                                        </li>
                                        <li class="cg-article__item">
                                            Delivering <b>customer experience</b> with efficient processes that carry insights between all revenue-driving teams becomes a huge competitive advantage.
                                        </li>
                                    </ol>
                                    <p class="cg-article__desc">
                                    According to a report by Forrester on ‘State of Revenue Operations in 2020’, the top goals of a business to create sustainable long-term growth, was in alignment with the three major shifts that recurring revenue model brings - improving internal and external efficiencies, and delivering smooth customer experience.
                                    </p> 
