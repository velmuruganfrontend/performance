                            <div class="cg-article">
                                <div class="cg-article__header">
                                    <h3 class="cg-article__title">
                                       Sustenance 
                                    </h3>
                                </div>
                                <div class="cg-article__content">
                                    <p class="cg-article__desc">
                                        SaaS businesses need to continuously streamline their base for retention and expansion from existing customers even as they continue to add new revenue at the top of the funnel. Without this base, businesses run the risk of having a “leaky bucket” problem, where they have to continuously plug holes by adding new customers just to offset the loss from churn. 
                                    </p>
                                    <p class="cg-article__desc">
                                        “Over the course of the year where you start with 100 customers or $100/MRR but have 5% monthly churn, you’d need to acquire 46 customers (or $46/MRR) just to break even with the beginning of the year. To grow by just 1 customer you’d have to acquire 47 customers!”
                                    </p>
                                    <p class="cg-article__desc text-right">
                                        -  Excerpt from <a class="cg-link" href="https://sixteenventures.com/saas-churn-rate">sixteen ventures on acceptable churn rate</a>
                                    </p>
                                    <figure class="cg-article__figure" style="margin-bottom: 10px;margin-top: 50px;">   
                                        <img src="/static/website/version/images/resources/guides/revenue-operations-saas/revops8.png" alt="saas revenue management challenges" class="img-responsive" />
                                    </figure>
                                    <p class="cg-article__desc text-center">
                                    Source: <a href="/resources/guides/subscription-revenue-management-report/" class="cg-link">State of Revenue Operations 2020</a>
                                    </p>
                                    <p class="cg-article__desc">
                                        Businesses leak revenue and are unable to maximize their revenue potential due to three major reasons:
                                    </p>
                                    <ol class="cg-article__list">
                                        <li class="cg-article__item">
                                            <h4>Information silos between teams: </h4>
                                            <p class="cg-article__desc">
                                                At startups, individual departments are loosely defined, at best. With nascent processes, information flow across members and teams is free-flowing. As the business matures and functions get bigger, they also grow farther apart. 
                                            </p>
                                        </li>
                                        <li class="cg-article__item">
                                            <h4>Systemic Dependencies between tools: </h4>
                                            <p class="cg-article__desc">
                                               The tools at a startup are markedly different from those at a business clocking even a million dollars. Without the infrastructure that allows for rapid flexibility, businesses get stuck in band-aids that eventually nibble away at growth.
                                            </p>
                                        </li>
                                        <li class="cg-article__item">
                                            <h4>Workflow gaps in how processes are set up: </h4>
                                            <p class="cg-article__desc">
                                                Multiple departments within the organization do not scale at the same rate and time. Having multiple teams bound together by systems and processes gets in the way of each function scaling independent of the other. 
                                            </p>
                                        </li>
                                    </ol>
                                    
