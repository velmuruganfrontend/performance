                                        Volume model
                                    </h2>
                                </div>

                                <div class="cg-article__content">
                                    <p class="cg-article__desc">
                                        Volume Pricing defines the price of the product with respect to the quantity purchased. Typically, the product price decreases with increase in quantity. The more the merrier (cheaper) holds true here.
                                    </p>
                                    <p class="cg-article__desc">
                                        <strong>Fictitious scenario:</strong> DrewCraft, a UX prototyping tool for designers, offers single and multi-user licenses for businesses. When a user signs up for the product, he can choose the number of licenses he wants to subscribe to. The price depends on the pricing tier the total number of licenses fall under.
                                    </p>
                                    <figure class="cg-article__figure" style="margin-top: 24px; margin-bottom: 72px;">
                                        <img src="/static/website/version/images/resources/guides/subscription-pricing-trial-strategy/group-5.png" alt="" class="img-responsive" width="480" />
                                    </figure>
                                    <p class="cg-article__desc">
                                        Unlike Tiered Pricing, where the product price of each unit may differ based on the unit range, in the case of Volume Pricing, all units of the product will cost the same.
                                    </p>
                                    <p class="cg-article__desc">
                                        <strong>Case 1:</strong> For a subscription created with 5 units, all units will be charged $10 per unit, as they will fall into the ‘volume 1’ category. And an invoice will be generated for a total amount of $50 (5 x $10)
                                    </p>
                                    <p class="cg-article__desc">
                                        <strong>Case 2:</strong> For a subscription created with 20 units, all units will be charged $6 per unit, as they will fall into the ‘volume 4’ category. And an invoice will be generated for a total amount of $120 (20 x $6)
                                    </p>
                                    <p class="cg-article__desc">
                                        <strong>Ex</strong> -  Sketch, a design toolkit, has a volume-based licensing model and charges $89 per license per month for up to 2 licenses and the price drops to $69 per license per month for more than 20 licenses.
                                    </p>


