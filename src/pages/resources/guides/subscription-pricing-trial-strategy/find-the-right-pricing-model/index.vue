                                    </p>
                                    <p class="cg-article__desc">
                                        <strong>Fictitious scenario:</strong> ManageWiz, a product management tool for businesses, offers licenses for small and large teams at flexible prices. The final charge will depend on the per unit price defined for the corresponding license's tier.
