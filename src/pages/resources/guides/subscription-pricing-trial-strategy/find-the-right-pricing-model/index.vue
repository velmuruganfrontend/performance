                            How you charge is as important as how much you charge. And when you’re just starting out, figuring out how much to charge isn’t easy either.
                        </p>
                        <p class="cg-article__desc">
                            The most primitive way to charge, of course, is to add an additional cost on top of how much it costs to provide the service. This is called <strong>cost-plus pricing</strong> and is barely functional in the SaaS and Services realm. Here’s why: what happens when you’ve identified a marketing channel that works for you and you want to increase your spend on it? What happens when there’s a new hire? Your costs are bound to continually increase, and it’s hard to tune your pricing proportionally along with it.
                        </p>
                        <p class="cg-article__desc">
                            Then, there’s <strong>competitor-based pricing</strong>. From spending a couple of minutes on your competitor’s pricing page, you can get a ballpark figure of how much you should price your product. The logic behind it, is that if a customer is willing to spend $400 on a similar product, they should be willing to spend the same (if not more) on a better product, right? This is the easiest way to determine your pricing, with no mind-boggling mathematics involved &mdash; just logical decisions based on where your product sits in the market.
                        </p>
                        <p class="cg-article__desc">
                            But basing your pricing on someone else’s strategy means you’re building on top of a slippery slope. With no real understanding of which customer segment you’re selling to and how much they’re willing to pay for the value you offer, it’s extremely hard to develop a foolproof product and pricing vision.
                        </p>
                        <p class="cg-article__desc">
                            And then we have a strategy that almost every SaaS pricing connoisseur swears by - value-based pricing. And they all believe that determining the right value-metric is critical to ensure that your pricing will scale with your growth.
                        </p>



                        <blockquote class="cg-article__blockquote">
                            This could easily be called "Customer-Based Pricing" because that is effectively what it is. Instead of looking inwardly at your own company, or laterally towards your competitors, with value-based pricing you look outward.

