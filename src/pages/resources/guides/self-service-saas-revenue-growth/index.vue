                                        <h3 class="cg-article__heading">
                                            2. Drive Sales through Product Qualified Leads
                                        </h3>
                                        <p class="cg-article__desc">
                                            A Product Qualified Lead (PQL) is a lead who has reached pre-defined triggers and experienced meaningful value using your product and is more likely to become a customer than other leads.
                                        </p>
                                        <p class="cg-article__desc">
                                             <a href="https://tomtunguz.com/the-new-sales-hotness-the-product-qualified-lead-pql/" target="_blank" class="cg-link">
                                             PQLs are more likely to become a customer than other leads.</a> Unlike a Marketing Qualified Lead (MQL) which bases buying intent on arbitrary factors like email opens, whitepaper downloads, and webpage visits, PQLs are tied to realizing meaningful value.
                                        </p>
                                        <p class="cg-article__desc">
                                            Assume a typical B2B SaaS business with a heavily inbound acquisition model. Sure, they might have a sales function marking their marketing leads as “SQLs” (Sales Qualified Leads). But they also have a larger proportion of prospects who sign up for a free trial. By designing specific points that capture a prospect’s intent to make a purchase, the business is able to drive sales conversations through the product experience. Voila! <em>Product Qualified Leads</em>.
                                        </p>
