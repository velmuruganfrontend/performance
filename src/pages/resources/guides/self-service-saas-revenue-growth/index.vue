                                    <div style="padding-top: 120px;" id="virality">
                                        <h3 class="cg-article__heading">
                                            2. Capture Virality from within your Product
                                        </h3>
                                        <p class="cg-article__desc">
                                            Virality is how a new product, an app, or a tool is able to catch on rapidly and spread like a software-driven epidemic. It hits critical mass, and takes off to the point where thousands of users are infected by it.
                                        </p>
                                        <div class="cg-article__desc">
                                            <strong>Ingredients to identify and cultivate virality within your product::</strong>
                                            <ul class="cg-article__list" style="margin-top: 16px;">
                                                <li class="cg-article__item">
                                                    A problem or pain point exists for a large number of people.
                                                </li>
                                                <li class="cg-article__item">
                                                    Designed to realize “aha!” moments early in their journey.
                                                </li>
                                                <li class="cg-article__item">
                                                    Channels for users to advocate for your product and invite others.
                                                </li>
                                            </ul>
                                        </div>
                                        <p class="cg-article__desc">
                                            An essential part of engineering a viral experience lies in crafting channels that encourage and capture users to continue spreading the idea of your product. A truly viral product turns every user experience into a self-propagating marketing and sales machine.
                                        </p>
                                        <div class="cg-article__panel">
                                            <div class="cg-panel cg-panel--secondary">
                                                <div class="cg-panel__header">
                                                    Example
