                                    <h2 class="cg-article__title">
                                        Metrics to Track and Measure
                                    </h2>
                                    <blockquote class="cg-article__blockquote" style="font-size: 20px;">
                                        <p class="cg-article__desc">
                                            Most people use analytics the way a drunk uses a lamp post, for support rather than illumination.
                                        </p>

                                        <strong>David Ogilvy</strong>
                                    </blockquote>
                                </div>

                                <div class="cg-article__content">
                                    <p class="cg-article__desc">
                                        You can’t manage what you can’t measure. A self-service strategy relying greatly on the product, changes how companies track the effectiveness of customers moving through different lifecycle stages: from signup to PQL, from PQL to customer, and from customer to active customer.
                                    </p>
                                    <p class="cg-article__desc">
                                        Measuring the conversion rate from one state to the next helps companies understand what part of the lifecycle can be improved. For a self-service SaaS, there are seven states in the customer lifecycle:
                                    </p>
                                    <div class="cg-article__desc" style="margin-top: 12px">
                                        <ul class="cg-article__list" style="margin-top: 16px;">
                                            <li class="cg-article__item">
                                                Visitor
                                            </li>
                                            <li class="cg-article__item">
                                                Signup/Trial
                                            </li>
                                            <li class="cg-article__item">
                                                PQL
                                            </li>
                                            <li class="cg-article__item">
                                                Customer
                                            </li>
                                            <li class="cg-article__item">
                                                Active Customer
                                            </li>
                                            <li class="cg-article__item">
                                                Renewed/Retained
                                            </li>
                                            <li class="cg-article__item">
                                                Churned
                                            </li>
                                        </ul>
                                    </div>
                                    <p class="cg-article__desc">
                                        Now let’s take a closer look at some of the most important SaaS metrics for measuring self-service growth.
                                    </p>




                                    <div style="padding-top: 90px;">
                                        <h3 class="cg-article__heading">
                                            1. Virality
                                        </h3>
                                        <p class="cg-article__desc">
                                            Product virality occurs when a product’s rate of adoption increases exponentially with each additional user. Viral growth is measured using the viral coefficient—or the <a href="https://en.wikipedia.org/wiki/K-factor_(marketing)" target="_blank" class="cg-link">k factor</a> &mdash; which is calculated as:
                                        </p>
                                        <div class="cg-article__figure" style="margin-bottom: 42px;">
                                             <img src="/static/website/version/images/resources/guides/self-service-saas-revenue-growth/formula/c(0)-no-of-cus-2.png" width="500" alt="" class="img-responsive">
                                        </div>

                                        <p class="cg-article__desc">
                                            In order for virality to exist, k must be greater than 1.
                                        </p>
                                        <p class="cg-article__desc">
                                            <strong>C(0)</strong> = the number of customers at the beginning of the time period you're measuring
                                        </p>
                                        <p class="cg-article__desc">
                                            <strong>i</strong> = the number of invitations each customer shares
                                        </p>
                                        <p class="cg-article__desc">
                                            <strong>c</strong> = the percent conversion rate of each invitation
                                        </p>
                                        <p class="cg-article__desc">
                                            <strong>k </strong>= the number of new customers each existing customer can convert <strong>(i * c)</strong>
                                        </p>
                                        <p class="cg-article__desc">
                                            The product virality formula is calculated by multiplying the number of customers at the beginning of a time period by the viral k coefficient (which measures the conversion rate of new customers invited by existing customers to start using a product).
                                        </p>
                                    </div>





                                    <div style="padding-top: 90px;">
                                        <h3 class="cg-article__heading">
                                            2. Time to Value
                                        </h3>
                                        <p class="cg-article__desc">
                                            Time to value (TTV) is the amount of time it takes new users to reach their first <a href="https://www.appcues.com/blog/aha-moment-guide" target="_blank" class="cg-link">aha moment</a> or activation event.
                                        </p>
                                        <p class="cg-article__desc">
                                            <strong>Goal:</strong> The goal is to reduce TTV as much as possible—the closer it is to zero, the better.
                                        </p>
                                    </div>



                                    <div style="padding-top: 90px;">
                                        <h3 class="cg-article__heading">
                                            3. Product Qualified Leads
                                        </h3>
                                        <p class="cg-article__desc">
                                            Product-qualified leads (PQLs) are typically activated users—folks who have completed a key action within your product, had their aha moment, and have seen the value that your product can offer first-hand.
                                        </p>
                                        <p class="cg-article__desc">
                                            PQLs provide insights into customer intent and predict how likely a user is to progress to the next stage of the customer lifecycle.
                                        </p>
                                    </div>



                                    <div style="padding-top: 90px;">
                                        <h3 class="cg-article__heading">
                                            4. Average Revenue Per User (ARPU)
                                        </h3>
                                        <p class="cg-article__desc">
                                            Average revenue per user (ARPU) is the amount of money, on average, that you can expect to make from an individual user. It is a good indicator of the overall health of your business.
                                        </p>


                                        <p class="cg-article__desc">
                                           <strong>How to calculate</strong>
                                        </p>
                                        <div class="cg-article__figure" style="margin-bottom: 42px;">
                                             <img src="/static/website/version/images/resources/guides/self-service-saas-revenue-growth/formula/ARPU.png" width="420" alt="" class="img-responsive">
                                        </div>
                                    </div>




                                    <div style="padding-top: 120px;">
                                        <h3 class="cg-article__heading">
                                            5. Customer Lifetime Value
                                        </h3>
                                        <p class="cg-article__desc">
                                            Customer lifetime value (CLV or LTV) is a prediction of how much revenue your business will receive from a single customer over the duration of your relationship.
                                        </p>
                                       <p class="cg-article__desc">
                                           <strong>The simple CLV formula:</strong>
                                       </p>
                                        <div class="cg-article__figure" style="margin-bottom: 42px;">
                                             <img src="/static/website/version/images/resources/guides/self-service-saas-revenue-growth/formula/annual-revenue.png" width="500" alt="" class="img-responsive">
                                        </div>

                                        <div class="cg-article__desc" >
                                            <strong style="display: inline-block; margin-bottom: 18px;">Understanding CLV can help your business answer these critical questions:</strong>
                                            <ul class="cg-article__list" style="margin-top: 16px;">
                                                <li class="cg-article__item">
                                                    How much can we comfortably spend on marketing and sales for customer acquisition?
                                                </li>
                                                <li class="cg-article__item">
                                                    How much should we spend on customer service to retain an existing customer?
                                                </li>
                                                <li class="cg-article__item">
                                                    Who are our most valuable customers and how can we better target this demographic for future sales?
                                                </li>
                                            </ul>
                                        </div>
                                    </div>






                                    <div style="padding-top: 120px;">
                                        <h3 class="cg-article__heading">
                                            6. Net Revenue Churn
                                        </h3>
                                        <p class="cg-article__desc">
                                            Net Revenue Churn is the percentage of revenue you have lost from existing customers in a period by total revenue at the beginning of a period.
                                        </p>
                                         <div class="cg-article__figure" style="margin-bottom: 42px;">
                                             <img src="/static/website/version/images/resources/guides/self-service-saas-revenue-growth/formula/net-revenue-churn.png" width="500" alt="" class="img-responsive">
                                        </div>
                                        <div class="cg-article__desc" style="margin-top: 30px;">
                                            <strong style="display: inline-block; margin-bottom: 18px;">Understanding Net Revenue Churn:</strong>
                                            <ul class="cg-article__list" style="margin-top: 16px;">
                                                <li class="cg-article__item">
                                                    A strongly negative net revenue churn means that a company’s revenue would increase even if there were no new sales.
                                                </li>
                                                <li class="cg-article__item">
                                                    If net revenue churn is near zero, then churn erases the growth from upsells, so all growth must come from new customers.
                                                </li>
                                                <li class="cg-article__item">
                                                    If it’s positive, the company’s sales team is working hard to fill a leaky bucket.
                                                </li>
                                            </ul>
                                        </div>
                                    </div>




                                    <div style="padding-top: 120px;">
                                        <h3 class="cg-article__heading">
                                            7. Expansion Revenue
                                        </h3>
                                        <p class="cg-article__desc">
                                            Expansion revenue—also called expansion MRR—measures the revenue generated from existing customers through upsells, add-ons, cross-sells, etc.
                                        </p>
                                        <p class="cg-article__desc">
                                            ProfitWell recommends that at least <a href="https://www.priceintelligently.com/blog/saas-subscription-expansion-revenue-is-crucial" target="_blank" class="cg-link">30% of your revenue</a> should be expansion revenue—but really, the higher the better.
                                        </p>

                                        <blockquote class="cg-article__blockquote" style="font-size: 20px;">
                                            <p class="cg-article__desc">
                                                Customers leave. Churn is inevitable. Even if you can get it down >1%, it's still going to hurt your growth. Acquisition can't fight it. The only thing that can is expansion.
                                            </p>
                                            <strong>Patrick Campbell, Co-founder and CEO, Profitwell</strong>
                                        </blockquote>
                                        <div class="cg-article__desc" style="margin-top: 30px;">
                                            <strong style="display: inline-block; margin-bottom: 18px;">When it comes to metrics for measuring PLG, there are plenty of other numbers to look at. But it all comes down to: </strong>
                                            <ul class="cg-article__list" style="margin-top: 16px;">
                                                <li class="cg-article__item">
                                                    What does it cost you to acquire a dollar in gross profit?
                                                </li>
                                                <li class="cg-article__item">
                                                    What happens to that dollar over time?
                                                </li>
                                            </ul>
                                        </div>
                                        <p class="cg-article__desc">
                                            The most important thing when it comes to measuring PLG metrics is that you keep it simple and then make sure every department is aligned around the one or two metrics that really matter.
                                        </p>
                                        <p class="cg-article__desc">
                                            If you’re drowning in spreadsheets but aren’t iterating on your product, you’re probably doing it wrong. The above mentioned metrics can help for starters.
                                        </p>
                                    </div>

