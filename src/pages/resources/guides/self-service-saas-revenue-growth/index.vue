                                            <div class="cg-panel cg-panel--secondary">
                                                <div class="cg-panel__header">
                                                    Example
                                                </div>
                                                <div class="cg-panel__content">
                                                    <div class="cg-panel__figure" style="margin-bottom: 24px;">
                                                        <img src="/static/website/version/images/customers/logos/freedom.png" alt="" class="img-responsive" width="180">
                                                    </div>
                                                    <p class="cg-article__desc">
                                                        <a href="https://freedom.to" target="_blank" class="cg-link">Freedom</a> is a website blocker that allows over 450,000 users to enjoy a distraction-free experience. With a solution for a pain-point affecting the productivity of millions of people every day, Freedom had a head-start on word-of-mouth marketing.
                                                    </p>
                                                    <p class="cg-article__desc">
                                                        Fred Stutzman wrote the first version of Freedom back in 2008 when he wanted to keep himself away from distracting sites. He shared the tool with a few people, and it just took off from there. People naturally wanted to rave about the app that kept them focused on what matters without getting sucked into the Internet black hole. Within a year, with zero marketing, Fred earned over half a million users, just through word of mouth.
                                                    </p>
