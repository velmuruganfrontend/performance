<script>
    import AppMeta from '~/components/AppMeta.vue';
    import AppHeader from '~/components/AppHeader.vue';
    import AppFooter from '~/components/AppFooter.vue';
    
    export default {
        components: {
            'app-meta': AppMeta,
            'app-header': AppHeader,
            'app-footer': AppFooter
        },
        methods: {},
        mounted(){
            //For animation on scroll
            var $window           = $(window),
                win_height_padded = $window.height() * 1.1;
            function revealOnScroll() {
                var scrolled = $window.scrollTop(),
                    win_height_padded = $window.height() * 1.1;
                // Showed...
                $(".revealOnScroll:not(.animate)").each(function () {
                var $this     = $(this),
                    offsetTop = $this.offset().top;
                if (scrolled + win_height_padded > offsetTop) {
                    $this.addClass('animate ' + $this.attr('data-animate'));
                }
                });
            }
            $window.on('scroll', revealOnScroll);



            //For mobile show/hide all feature
            $('[data-yr-action]').click(function() {
                $('[data-yr-card]').removeClass('cb-yr__card--active');
                $(this).closest('[data-yr-card]').addClass('cb-yr__card--active');
                $('html, body').animate({scrollTop: $(this).offset().top - 100}, 0);
                $("#cb-js-pricing-mheader").addClass("l-main__mfixed--active");
                $("#cyr-mheader-control").val($(this).attr('data-yr-action'));
            });
            $("#cyr-mheader-control").change(function() {
                var temp = $("#cyr-mheader-control").val().toLowerCase();
                $('[data-yr-card]').removeClass('cb-yr__card--active');
                var mbSelector = '[data-mb-trigger="'+ temp +'"]';
                $(mbSelector).addClass('cb-yr__card--active');
                $('html, body').animate({scrollTop: $(mbSelector).offset().top - 100}, 200);
            });

            //Scrollspy 
            $('body').scrollspy({ target: '#ca-nav-affix'});

            $("#ca-nav-affix li a[href^='#']").on('click', function(e) {
                // prevent default anchor click behavior
                e.preventDefault();

                // store hash
                var hash = this.hash;

                // animate
                $('html, body').animate({
                    scrollTop: $(this.hash).offset().top
                }, 1000, function(){

                // when done, add hash to url
                // (default click behaviour)
                window.location.hash = hash;
                });
            });

            //Affix scroll
            $('[data-affix-after]').each( function() {
                var elem = $(this);
                var parent_panel = elem.parent();
                var prev = $( elem.data('affix-after') );

                if( prev.length != 1 ) {
                /* Create a new element immediately before. */
                prev = elem.before( '<div></div>' ).prev();
                }

                var resizeFn = function() {
                /* Set the width to it's natural width in the parent. */
                var sideBarNavWidth = parent_panel.width()
                    - parseInt(elem.css('paddingLeft'))
                    - parseInt(elem.css('paddingRight'))
                    - parseInt(elem.css('marginLeft'))
                    - parseInt(elem.css('marginRight'))
                    - parseInt(elem.css('borderLeftWidth'))
                    - parseInt(elem.css('borderRightWidth'));
                elem.css('width', sideBarNavWidth);

                elem.affix({
                    offset: {
                        top: prev.offset().top + prev.outerHeight(true) - 30,
                        bottom: function () {
                            return (this.bottom = $('#cb-affix-footer').outerHeight(true) + 150)
                        }
                    }
                });
                };

                resizeFn();
                    $(window).resize(resizeFn);
            });
        }
    }
</script>
