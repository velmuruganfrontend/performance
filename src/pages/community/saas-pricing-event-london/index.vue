        methods: {},
        mounted(){
            $(document).ready(function(){
                //main Slider
                $('#ce-main-slider').carousel({
                    interval: false
                });
                var totalSliderItems = $('.ce-slider__item').length;
                var currentSliderIndex = $('.ce-slider__item.active').index() + 1;
                $('#ce-slider-number').html(''+currentSliderIndex+'/'+totalSliderItems+'');


                $('#ce-main-slider').bind('slid.bs.carousel', function() {
                   currentSliderIndex = $('.ce-slider__item.active').index() + 1;
                   $('#ce-slider-number').html(''+currentSliderIndex+'/'+totalSliderItems+'');
                });



                //Attendees slider 
                var totalItems = $('.ce-attendees__list').length;
                var currentIndex = $('.ce-attendees__list.active').index() + 1;
                $('#ce-js-number').html(''+currentIndex+'/'+totalItems+'');


                $('#ce-attendees-slider').bind('slid.bs.carousel', function() {
                   currentIndex = $('.ce-attendees__list.active').index() + 1;
                   $('#ce-js-number').html(''+currentIndex+'/'+totalItems+'');
                });

                $('#ce-attendees-slider').carousel({
                    interval: false,
                    wrap: false
                });


                //For animation on scroll
                var $window           = $(window),
                    win_height_padded = $window.height() * 1.1;
                function revealOnScroll() {
                    var scrolled = $window.scrollTop(),
                        win_height_padded = $window.height() * 1.1;
                    // Showed...
                    $(".revealOnScroll:not(.animate)").each(function () {
                      var $this     = $(this),
                          offsetTop = $this.offset().top;
                      if (scrolled + win_height_padded > offsetTop) {
                        $this.addClass('animate ' + $this.attr('data-animate'));
                      }
                    });
                  }
                  $window.on('scroll', revealOnScroll);
            });

            //scroll top on click of sidebar items
            $('[data-sidebar-item]').find("[data-link]").each(function(index) {
                $(this).click(function() {
                    var attr = $(this).attr("data-link");
                    $("body, html").animate({
                        scrollTop: $("#"+attr).offset().top - 100
                    });
                });
            });
            
        }
