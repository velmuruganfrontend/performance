        mounted(){
            // For Slider in page
            $('.cc-slider__item:first-child').addClass('cc-slider__item-active');
            $('[set_url]').attr('src',$('.cc-slider__item-active').attr('get_url')).removeClass('cc-slider__animate').addClass('cc-slider__animate');
            var activeItem;
            function slider(){
                activeItem = $('.cc-slider__item-active').next('.cc-slider__item');
                if(activeItem.length){
                    $('.cc-slider__item').removeClass('cc-slider__item-active');
                    $(activeItem).addClass('cc-slider__item-active');
                }else{
                    $('.cc-slider__item').removeClass('cc-slider__item-active');
                    $('.cc-slider__item:first-child').addClass('cc-slider__item-active');
                }
                $('[set_url]').attr('src',$('.cc-slider__item-active').attr('get_url')).removeClass('cc-slider__animate').addClass('cc-slider__animate');
            }
            var startSlider = setInterval(slider, 4000);
            $('.cc-slider__item .cc-slider__heading').on('click', function(){
                clearInterval(startSlider);
                $('.cc-slider__item .cc-slider__heading').parents('.cc-slider__item').removeClass('cc-slider__item-active');
                $(this).parents('.cc-slider__item').addClass('cc-slider__item-active').find('.cc-slider__progress').addClass('cc-slider__progress--hide');
                //startSlider = setInterval(slider, 4000);
                $('[set_url]').attr('src',$('.cc-slider__item-active').attr('get_url')).removeClass('cc-slider__animate').addClass('cc-slider__animate');
            })
            setTimeout(function(){
                $('.cp-hero__highlight').addClass('cp-hero__highlight--active')
            }, 750)
            var activeTab;
            $('.cc-tab:first-child').addClass('active');
            $('.cc-tab__content .tab-pane:first-child').addClass('in active');
            function tabAnimation(){
                activeTab = $('.cc-tab.active').next('.cc-tab');
                $('.cc-tab.active').removeClass('active');
                $('.cc-tab__content .tab-pane').removeClass('in active');
                if(activeTab.length){
                    activeTab.addClass('active');
                    $('.cc-tab__content '+activeTab.find('a').attr('href')).addClass('in active');
                }else{
                    $('.cc-tab:first-child').addClass('active');
                    $('.cc-tab__content .tab-pane:first-child').addClass('in active');
                }
            }
            $('.cc-tab a').on('click', function(){
                clearInterval(startTabAnimation);
                $('.cc-tab').addClass('stopped-animation');
            })
            var startTabAnimation = setInterval(tabAnimation, 8000);
            // For youtube video embed
            if($('.cp-modal__video')) {
                $('.cp-modal__video').click(function () {
                      var theModal = $(this).data("target"),
                    videoSRC = $(this).attr("data-video"),
                    videoSRCauto = videoSRC + "?modestbranding=1&rel=0&controls=1&showinfo=0&html5=1&autoplay=1";
                    $(theModal + ' iframe').attr('src', videoSRCauto);
                    $(theModal).click(function () {
                      $(theModal + ' iframe').attr('src', videoSRC);
                    });
                });
            } 
        }
    }
